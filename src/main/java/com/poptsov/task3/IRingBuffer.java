package com.poptsov.task3;

/**
 * Интерфейс для кольцевого буфера (FIFO)
 * @param <T> тип элементов буфера
 */
public interface IRingBuffer<T> {
    /**
     * Добавляет элемент в буфер.
     * Если буфер полон, перезаписывает самый старый элемент.
     * @param item элемент для добавления
     */
    void put(T item);

    /**
     * Извлекает и удаляет элемент из буфера.
     * @return элемент или null, если буфер пуст
     */
    T get();

    /**
     * Проверяет, пуст ли буфер.
     * @return true, если буфер пуст
     */
    boolean isEmpty();

    /**
     * Проверяет, заполнен ли буфер.
     * @return true, если буфер полон
     */
    boolean isFull();

    /**
     * Возвращает текущее количество элементов в буфере.
     * @return количество элементов
     */
    int size();
}